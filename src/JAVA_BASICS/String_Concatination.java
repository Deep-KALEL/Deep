package JAVA_BASICS;

public class String_Concatination {

	public static void main(String[] args) {

		//String Concatination
		int a = 100;
		int b = 200;
		String x = "Dipak";
		String y = "Kalel";
		System.out.println(a+b);
		System.out.println(a+b+y);
		System.out.println(a+b+x);
		System.out.println(a+b+x+y);
		
	}

}
