package JAVA_BASICS;

public class Loops {

	public static void main(String[] args) {

		// while loop
		// Que. prints the number from 0 to 10

		int i = 0;
		while (i <= 10) {
			// System.out.println(i);
			++i;
		}

		int a = 10;
		int b = ++a;// pre increment
	//	int b = a++;// post increment
	//	b = a;
		System.out.println(b);
		
		
		

	}

}
