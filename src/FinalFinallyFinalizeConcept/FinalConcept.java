package FinalFinallyFinalizeConcept;

public class FinalConcept {
//final keyword
	//final keyword is used to create constant value.
	// Also final keyword also used to prevent inheritance.
	public static void main(String[] args) {
		final int i = 10;// final keyword is used to create constant value.
		i = 20;
		i = 30;
	}

}
